public class SignIn {

    private int sid;
    private int uid;
    private String college;
    private String captain;
    private String duty;
    private String phone;
    private String email;
    private String address;

    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public String getCollege() {
        return college;
    }

    public void setCollege(String college) {
        this.college = college;
    }

    public String getCaptain() {
        return captain;
    }

    public void setCaptain(String captain) {
        this.captain = captain;
    }

    public String getDuty() {
        return duty;
    }

    public void setDuty(String duty) {
        this.duty = duty;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        SignIn signIn = (SignIn) o;

        if (sid != signIn.sid) return false;
        if (uid != signIn.uid) return false;
        if (college != null ? !college.equals(signIn.college) : signIn.college != null) return false;
        if (captain != null ? !captain.equals(signIn.captain) : signIn.captain != null) return false;
        if (duty != null ? !duty.equals(signIn.duty) : signIn.duty != null) return false;
        if (phone != null ? !phone.equals(signIn.phone) : signIn.phone != null) return false;
        if (email != null ? !email.equals(signIn.email) : signIn.email != null) return false;
        return address != null ? address.equals(signIn.address) : signIn.address == null;
    }

    @Override
    public int hashCode() {
        int result = sid;
        result = 31 * result + uid;
        result = 31 * result + (college != null ? college.hashCode() : 0);
        result = 31 * result + (captain != null ? captain.hashCode() : 0);
        result = 31 * result + (duty != null ? duty.hashCode() : 0);
        result = 31 * result + (phone != null ? phone.hashCode() : 0);
        result = 31 * result + (email != null ? email.hashCode() : 0);
        result = 31 * result + (address != null ? address.hashCode() : 0);
        return result;
    }

    @Override
    public String toString() {
        return "SignIn{" +
                "sid=" + sid +
                ", uid=" + uid +
                ", college='" + college + '\'' +
                ", captain='" + captain + '\'' +
                ", duty='" + duty + '\'' +
                ", phone='" + phone + '\'' +
                ", email='" + email + '\'' +
                ", address='" + address + '\'' +
                '}';
    }
}
